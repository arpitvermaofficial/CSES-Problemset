void solve()
{
    /*
    Approach
    We use two pointer with hashmap
    in two pointer approach no of subarray bw i and j is distance
    bw i and j
    in this solution we move i till m have distict elements equal to k
    and then move pointer j till map size is less than k
    and count the no of subarray by i-j formula
    */
    ll n, k;
    cin >> n >> k;
    vector<ll> v(n);
    for (ll i = 0; i < n; i++)
    {
        cin >> v[i];
    }
    ll ans = 0;
    ll i = -1, j = -1;
    map<ll, ll> m;

    while (true)
    {
        while (i < n)
        {
            i++;
            if (i < n)
            {
                m[v[i]]++;

                if (m.size() <= k)
                {
                    ans += i - j;
                }
                else
                {
                    break;
                }
            }
        }

        if (i == n && m.size() <= k)
        {
            break;
        }

        while (j < i)
        {
            j++;
            if (m[v[j]])
            {
                m[v[j]]--;
                if (m[v[j]] <= 0)
                {
                    m.erase(v[j]);
                }
            }
            else
            {

                m[v[j]]++;
            }
            if (m.size() > k)
            {
                continue;
            }
            else
            {
                ans += i - j;
                break;
            }
        }
    }
    print(ans);
}

